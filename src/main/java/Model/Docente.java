
package Model;

public class Docente {

    private int codigoDcte;
    private String nomDcte;
    private String apeDcte;
    private String correoDcte;
    private int idUsu;

    // Getters y Setters
    public int getCodigoDcte() {
        return codigoDcte;
    }

    public void setCodigoDcte(int codigoDcte) {
        this.codigoDcte = codigoDcte;
    }

    public String getNomDcte() {
        return nomDcte;
    }

    public void setNomDcte(String nomDcte) {
        this.nomDcte = nomDcte;
    }

    public String getApeDcte() {
        return apeDcte;
    }

    public void setApeDcte(String apeDcte) {
        this.apeDcte = apeDcte;
    }

    public String getCorreoDcte() {
        return correoDcte;
    }

    public void setCorreoDcte(String correoDcte) {
        this.correoDcte = correoDcte;
    }

    public int getIdUsu() {
        return idUsu;
    }

    public void setIdUsu(int idUsu) {
        this.idUsu = idUsu;
    }

}

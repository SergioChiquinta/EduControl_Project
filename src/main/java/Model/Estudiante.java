
package Model;

public class Estudiante {

    private int codigoStu;
    private String nomStu;
    private String apeStu;
    private String correoStu;
    private int codigoDcte;
    private int idUsu;

    // Getters y Setters
    public int getCodigoStu() {
        return codigoStu;
    }

    public void setCodigoStu(int codigoStu) {
        this.codigoStu = codigoStu;
    }

    public String getNomStu() {
        return nomStu;
    }

    public void setNomStu(String nomStu) {
        this.nomStu = nomStu;
    }

    public String getApeStu() {
        return apeStu;
    }

    public void setApeStu(String apeStu) {
        this.apeStu = apeStu;
    }

    public String getCorreoStu() {
        return correoStu;
    }

    public void setCorreoStu(String correoStu) {
        this.correoStu = correoStu;
    }

    public int getCodigoDcte() {
        return codigoDcte;
    }

    public void setCodigoDcte(int codigoDcte) {
        this.codigoDcte = codigoDcte;
    }

    public int getIdUsu() {
        return idUsu;
    }

    public void setIdUsu(int idUsu) {
        this.idUsu = idUsu;
    }

}
